PHP provides a wide range of built-in functions for working with arrays. Here is a list of all the array functions available in PHP as of version 8.1:

array(): Create an array.
array_change_key_case(): Changes all keys in an array to lowercase or uppercase.
array_chunk(): Split an array into chunks of smaller arrays.
array_column(): Return the values from a single column in the input array.
array_combine(): Create a new array by using one array for keys and another for its values.
array_count_values(): Count the number of occurrences of each value in an array.
array_diff(): Compare arrays and return the values that are present in one array but not the other.
array_diff_assoc(): Compare arrays and return the values that are present in one array but not the other, including key comparisons.
array_diff_key(): Compare arrays and return the values that are present in one array but not the other, based on keys.
array_diff_uassoc(): Compare arrays and return the values that are present in one array but not the other, including key comparisons using a user-defined function.
array_diff_ukey(): Compare arrays and return the values that are present in one array but not the other, based on keys using a user-defined function.
array_fill(): Fill an array with values.
array_fill_keys(): Fill an array with values, specifying keys.
array_filter(): Filter an array using a callback function.
array_flip(): Exchange all keys with their associated values in an array.
array_intersect(): Compare arrays and return the values that are present in all arrays.
array_intersect_assoc(): Compare arrays and return the values that are present in all arrays, including key comparisons.
array_intersect_key(): Compare arrays and return the values that are present in all arrays, based on keys.
array_intersect_uassoc(): Compare arrays and return the values that are present in all arrays, including key comparisons using a user-defined function.
array_intersect_ukey(): Compare arrays and return the values that are present in all arrays, based on keys using a user-defined function.
array_key_exists(): Check if a key exists in an array.
array_keys(): Return all the keys or a subset of the keys of an array.
array_key_first(): Return the first key of an array.
array_key_last(): Return the last key of an array.
array_map(): Apply a callback function to each element of an array and return a new array with the results.
array_merge(): Merge two or more arrays into a single array.
array_merge_recursive(): Merge two or more arrays into a single array recursively.
array_multisort(): Sort multiple arrays or multi-dimensional arrays.
array_pad(): Pad an array to a specified length with a specified value.
array_pop(): Remove the last element from an array.
array_product(): Return the product of all values in an array.
array_push(): Add one or more elements to the end of an array.
array_rand(): Pick one or more random keys from an array.
array_reduce(): Reduce an array to a single value using a callback function.
array_replace(): Replace the values of one array with the values of another array.
array_replace_recursive(): Replace the values of one array with the values of another array recursively.
array_reverse(): Reverse the order of the elements in an array.
array_search(): Search an array for a given value and return the corresponding key if successful.
array_shift(): Remove the first element from an array.
array_slice(): Extract a slice of an array.
array_splice(): Remove a portion of an array and replace it with something else.
array_sum(): Return the sum of all the values in an array.

